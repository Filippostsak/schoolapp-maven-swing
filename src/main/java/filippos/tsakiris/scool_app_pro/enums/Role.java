package filippos.tsakiris.scool_app_pro.enums;

/**
 * Enumeration for representing roles within the school application.
 * <p>
 * This enum defines the following roles:
 * <ul>
 *     <li>{@code Student} - Represents a student role, typically assigned to users who are students.</li>
 *     <li>{@code Teacher} - Represents a teacher role, typically assigned to users who are teachers.</li>
 * </ul>
 * <p>
 * It is utilized to differentiate between different types of users and their access levels within the application.
 */
public enum Role {
    /**
     * Role assigned to users who are students.
     */
    Student,

    /**
     * Role assigned to users who are teachers.
     */
    Teacher
}
