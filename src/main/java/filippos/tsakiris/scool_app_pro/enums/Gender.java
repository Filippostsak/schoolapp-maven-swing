package filippos.tsakiris.scool_app_pro.enums;

/**
 * Enumeration for representing gender.
 * <p>
 * This enum has two possible values:
 * <ul>
 *     <li>{@code M} - Represents Male.</li>
 *     <li>{@code F} - Represents Female.</li>
 * </ul>
 * <p>
 * It can be used throughout the application to specify the gender of individuals where required.
 */
public enum Gender {
    /**
     * The Male gender.
     */
    M,

    /**
     * The Female gender.
     */
    F
}
